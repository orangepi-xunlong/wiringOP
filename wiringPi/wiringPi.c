{
	int alt;

	pin &= 63;

	if (wiringPiMode == WPI_MODE_PINS)
		pin = pinToGpio [pin];
	else if (wiringPiMode == WPI_MODE_PHYS)
		pin = physToGpio [pin];
	else if (wiringPiMode != WPI_MODE_GPIO)
		return 0;

	alt = OrangePi_get_gpio_mode(pin);

	return alt;
}

